    private void btnCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateActionPerformed
        // TODO add your handling code here:
        if (txtEnergy.getText().equals("") && txtSelectivity.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please enter the value for energy or selectivity!");
        } else {
            if (!txtEnergy.getText().equals("")) {
                float energy = Float.parseFloat(txtEnergy.getText());
                float temperature = (float) (Float.parseFloat(txtTemperature.getText()) + 273.15);
                float calc = (float) ((energy * 4.18 * 1000) / (8.3145 * temperature));
                float ratio = (float) Math.exp(-1 * calc);
                txtSelectivity.setText(String.format("%.2f", 1 / ratio));
            } else {
                float temperature = (float) (Float.parseFloat(txtTemperature.getText()) + 273.15);
                float ratio = Float.parseFloat(txtSelectivity.getText());
                float calc = (float) Math.log(1 / ratio);
                float energy = (float) ((float) ((float) -1 * calc * 8.3145 * temperature) / (4.18 * 1000));
                txtEnergy.setText(String.format("%.2f", energy));
            }
        }
    }//GEN-LAST:event_btnCalculateActionPerformed

